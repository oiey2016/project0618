export const getCatById = (id: string): Cat | undefined => {
  return CATS.find(cat => cat.id === id);
};

export const getCatsByRarity = (rarity: Cat['rarity']): Cat[] => {
  return CATS.filter(cat => cat.rarity === rarity);
};

export const getCatsByDish = (dishId: string): Cat[] => {
  return CATS.filter(cat => cat.favoriteDishes.includes(dishId));
};
